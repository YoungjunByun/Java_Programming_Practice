public class AssignOperTest {
    public static void main(String[] args) {
        //아래 수행의 결과를 예측하라
        int a = 1;
        ++a;
        a++;
        a += 1;
        a = a + 1;
        System.out.println("a = " + a);

        int b = 7;
        b--;
        --b;
        b=b-1;
        b-=1;
        System.out.println("b = " + b);
    }
}
