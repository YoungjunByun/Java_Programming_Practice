public class FourArithmetic {
    public static void main(String[] args){
        //두 개의 정수를 사칙연산하여 출력하는 JavaSE프로그램 만들어보기
        int a = 12; int b = 2;
        System.out.println("a+b="+(a+b)); //'+' = Concatenation 기호
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
    }
}
